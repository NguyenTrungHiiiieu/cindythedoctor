            <h2>3. Gum Problems</h2>
            <p>Do your gums bleed easily? Are they tender? Do you notice them pulling away from your teeth? You might have gum disease (gingivitis). A buildup of plaque, a sticky bacteria, below the gum line causes it. Left untreated, it can at some point cause bone loss, and your teeth might shift or become loose. That can make it harder to chew and even speak. To avoid gum disease, brush, floss, and rinse with an antiseptic mouthwash daily, and see your dentist for regular cleanings.</p>
