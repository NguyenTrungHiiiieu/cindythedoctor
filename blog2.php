            <p>These little holes in your teeth are bad news. You get them when a sticky bacteria, called plaque, builds up on your teeth, slowly destroying the hard outer shell, called enamel. Adults can also have problems with tooth decay at the gum line and around the edges of earlier fillings. To prevent it, brush your teeth at least twice a day with a fluoride toothpaste, limit snacks, floss daily, rinse with a fluoride mouthwash, and keep up with your dental appointments. Ask your dentist if you could benefit from a sealant.</p>
