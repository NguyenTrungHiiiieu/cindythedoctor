            <h2>Problem #2: Tooth Decay</h2>
            <p>Tooth decay (cavities) is the most common health ailment. It is especially common among people who lack regular access to health care. Tooth decay occurs when plaque combines with sugars and/or starches of the food you eat. The combination produces acids that attack tooth enamel. You can get cavities at any age; they aren't just for children. Aging and normal enamel erosion can cause them. So can dry mouth due to age, illness, or medications. The best way to prevent tooth decay is by brushing twice a day, flossing daily, and getting regular dental check-ups. Also, eat healthy foods and avoid high-sugar snacks and drinks. Ask your dentist about more ways to keep your teeth healthy.</p>
