            <h2>Problem #3:Gum (Periodontal) Disease</h2>
            <p>Gum disease is an infection in the gums around your teeth. It's also a major cause of adult tooth loss. Everyone's at risk for gum disease. But it's most common after age 30. Smoking is a significant risk factor along with health conditions such as diabetes and dry mouth. The symptoms include: Bad breath; Red, swollen, tender, or bleeding gums; Sensitive teeth; Pain when chewing. Gingivitis is the technical name of gum disease. Periodontitis is advanced gum disease. Regular dental check-ups, brushing, and flossing can prevent them. See your dentist if you have any signs of gum disease. Treatment can prevent problems such as tooth loss.</p>
